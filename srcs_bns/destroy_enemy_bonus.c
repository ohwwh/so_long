#include "so_long_bonus.h"

void	destroy_enemies(t_vars *vars)
{
	int	i;

	i = 1;
	while (vars->objs[i])
	{
		free(vars->objs[i]);
		vars->objs[i] = 0;
		i ++;
	}
	vars->objs_n = 1;
}
